using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace ClassAssignment
{
    public class Math
    {
        public static int multi(int input)
        {
            int multiAnswer = input * 2;
            return multiAnswer; 
         }

        public static int divi(int input)
        {
            int diviAnswer = input / 2;
            return diviAnswer;
        }

        public static int addi(int input)
        {
            int addiAnswer = input + 2;
            return addiAnswer;
        }
    }
}
